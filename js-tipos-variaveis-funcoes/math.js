console.log(Math.round(3.6)); //Math.round = arredonda
console.log(Math.round(3.4999));
console.log(Math.round(3.5)); //0.5 arredonda pra cima

console.log(Math.ceil(3.1)); //..ceil = sempre pra cima
console.log(Math.floor(3.99999)); //..floor = sempre pra baixo

console.log(Math.trunc(3.1416)); //..trunc = desconsidera decimais

console.log(Math.pow(3, 2)); //..pow = potenciação (3 elevado a 2)
console.log(Math.sqrt(16)); //..sqrt = raiz quadrada

console.log(Math.min(-1, -2, 0, 1, 2, 3)) //..min = menor número entre os argumentos
console.log(Math.max(-1, -2, 0, 1, 2, 3)) //..max = maior número entre os argumentos

console.log(Math.random()) //..random = numero aleatório entre 0 e 1